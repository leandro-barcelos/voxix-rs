fn enumerate_devices() -> Vec<String> {
    let library = VulkanLibrary::new().expect("no local Vulkan library/DLL");
    let instance =
        Instance::new(library, InstanceCreateInfo::default()).expect("failed to create instance");

    instance
        .enumerate_physical_devices()
        .expect("could not enumerate devices")
        .map(|device| device.properties().device_name.clone())
        .collect()
}

struct PhysicalDeviceSelect {
    selected: usize,
    devices: Vec<String>,
    tx: Sender<String>,
}

impl PhysicalDeviceSelect {
    fn new(tx: Sender<String>) -> Self {
        Self {
            selected: 0,
            devices: enumerate_devices(),
            tx,
        }
    }
}
#[allow(unused)]
impl eframe::App for PhysicalDeviceSelect {
    fn update(&mut self, ctx: &eframe::egui::Context, frame: &mut eframe::Frame) {
        let Self {
            selected,
            devices,
            tx,
        } = self;

        egui::CentralPanel::default().show(ctx, |ui| {
            ui.heading("Select a Physical Device");
            ui.vertical(|ui| {
                for (i, device) in devices.iter().enumerate() {
                    ui.radio_value(selected, i, device);
                }
            });
            ui.end_row();

            if ui.button("Done").clicked() {
                let selected_device = devices.get(*selected).cloned().unwrap_or_default();
                let _ = tx.send(selected_device); // Send the selected device name to the main function
                ctx.send_viewport_cmd(egui::ViewportCommand::Close); // Close the window
            }
        });
    }
}
